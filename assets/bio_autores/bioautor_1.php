        <div style="font-size: 1.6em; margin:3px; padding:5px;">
          <p>&nbsp;Novelista y dramaturgo japonés. Nació en Tokio, pero pronto se estableció en Mukden (actualmente Shenyang),
en Manchuria, donde su padre ejercía la medicina. Esta tierra de nadie, lejos de Japón, tuvo una influencia
considerable en Abe. En su infancia mostró interés, entre otras cosas, por la entomología y la literatura de
Franz Kafka. Regresó a Japón en 1941 y se matriculó en la facultad de Medicina de la Universidad de Tokio en
1943, pero se trasladó de nuevo a Manchuria para esperar el fin de la II Guerra Mundial, momento en que fue
repatriado. Acabó la carrera en 1948; un año antes se había financiado él mismo la publicación de un libro de
poemas.<br />&nbsp; Ese mismo año irrumpe en el panorama literario con La señal de tráfico al final de la calle. 1.
Tras una breve etapa como dramaturgo marxista, estableció un estilo característico,
absurdo y kafkiano, poblado por personajes alienados atrapados en situaciones estrambóticas,
vagamente simbólicas, a menudo inspiradas en las novelas policíacas, la ciencia ficción y otros géneros
populares. <br />&nbsp;Con El crimen de S. Karma (1951) obtuvo el Premio Akutagawa. Edad del hielo 4 (1959) abundaba
en las catástrofes ecológicas; La mujer de la arena (1962), que gira en torno a un entomólogo aficionado
atrapado por una extraña comunidad costera, se convirtió en una película de fama internacional.
El rostro ajeno (1964) relata la lucha de un hombre con el rostro desfigurado por recuperar una
identidad social, y El hombre caja (1973) presenta a un héroe que se retira a una caja de cartón.<br />&nbsp;
Otras novelas importantes de Abe son El mapa en ruinas (1967) y Encuentro secreto (1977).
Kobo Abe mantuvo su propia compañía de teatro en Tokio con obras como Vosotros también sois
culpables (1964) o Amigos (1967), además de escribir para la televisión y otros medios.
El Arca Sakura (1984) revela cierta falta de inspiración.<br /></p>
