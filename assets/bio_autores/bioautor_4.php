          <p>&nbsp;Poeta y crítico de arte español, nunca ha participado en agrupaciones generacionales y su obra se caracteriza por un extremado rigor y calidad.
          Nacido en Oviedo, desde muy niño reside en León, ciudad en donde ha desarrollado una amplia labor cultural.<br />&nbsp;
          Su primer libro de poemas fue Sublevación inmóvil (1966) y con él trata de escapar a cualquier restricción realista.<br />&nbsp;
          Siguieron Descripción de la mentira (1977), León en la mirada (1979) y Blues castellano (1982), que incluye poemas redactados casi veinte años antes.<br /> &nbsp;
          Lápidas (1987) le supuso un gran reconocimiento por parte de la crítica, y con Edad (1987),
          una recopilación de su poesía hasta el momento con algunos inéditos, obtuvo el Premio Nacional de Poesía.<br />&nbsp;
          Libro del frío (1992) volvió a confirmarle como uno de los poetas más importantes del siglo.<br />&nbsp;
          En 2000 publicó la antología Sólo luz y en 2003 salió a la luz una reedición del Libro del frío, con la incorporación de veinte poemas nuevos.<br />&nbsp;
          También ha publicado estudios monográficos de escultores, como José Luis Sánchez (1981) y Julio Hernández (1981), y de los pintores Francisco Echanz (1978)
          y Juan Barjola (1980)<br /></p>
