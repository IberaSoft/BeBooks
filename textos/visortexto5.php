          <p> Voy en tren leyendo tranquilamente cuando sube una chica guapísima que se sienta frente a mí. Bueno, no exactamente frente a mí, yo estoy sentado junto a la puerta y estoy a unos cuantos metros de la chica, pero desde aquí se la puede ver muy bien. Qué guapa es, así da gusto viajar en tren, aunque no haya quien lea. Va hablando con el novio o sucedáneo, al que sólo veo la espalda desde aquí y ya me parece demasiado. Lo que estoy leyendo es interesante, pero la chica está bastante mejor. Como ya he determinado que es guapa, empiezo a mirarle las piernas aprovechando que lleva una falda muy corta. Bonitas piernas. Hace un rato que simulo estar leyendo, soy un voyeur ilustrado. Si fuera fea, Míchel, no estudiarías sus piernas con tanta atención. Es verdad, lo admito, pero un rostro bonito es una invitación a explorar el resto: «vale, ya sabemos que eres preciosa, veamos ahora lo demás». Sé que es un argumento muy endeble, pero ahora mismo es el mejor que tengo. Además, ¿es que no has visto a la chica?<br />
La chica separa un poco las piernas. Bragas verdes. ¿Se te ocurre algo mejor que la ropa interior femenina, Míchel? Alguna cosa se me ocurre, tengo mucha imaginación. Ya, lo que pasa es que eres un pervertido y estás pensando en guarradas. Oye, ¿pero con quién te crees que estás hablando? Conmigo mismo, supongo. Vale, vale, touché.<br />
El tren gira a la izquierda y un rayo de sol ilumina brevemente la entrepierna de la chica. Quizá sean azules.<br /></p>
