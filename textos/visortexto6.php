          <p>  —Marta, no me voy a levantar de la cama hoy.<br />
—¿Y eso?<br />
—Estoy muerto.<br />
—No exageres tanto, has tenido toda la noche para descansar.<br />
—No me entiendes, digo que estoy muerto en sentido literal.<br />
—Pues yo te veo hablando y respirando.<br />
—Eso es un engaño de los sentidos, ¿no has leído a Descartes?<br />
—No digas tonterías; lo que pasa es que no quieres ir a trabajar, pero a mí no me la das con queso.<br />
—Los muertos no mienten, cariño.<br />
—Voy a llamar al médico, no te vas a librar tan fácilmente.<br />
Marta llama al médico de la familia, que acude inmediatamente acompañado de una enfermera.<br />
—¿Qué es eso de que Miguel ha muerto? —pregunta el doctor.<br />
—Eso dice él —contesta Marta.<br />
El médico ausculta a Miguel y le hace decir treinta y tres.<br />
—¿Ve, doctor? —interviene Marta—. ¿Un muerto diría treinta y tres?<br />
—Eso lo decidiré yo, que soy el médico. Apunta, Virtudes: el difunto goza de buena salud.<br />
Luego conecta unos electrodos en los pezones de Miguel y aplica pequeñas descargas a intervalos regulares.<br />
—Sólo para ver cómo responde —explica.<br />
—¿Y bien, doctor? —pregunta Marta cuando finaliza el examen.<br />
—Pues yo diría que está vivo, pero quizá un genio maligno está falseando los resultados de las pruebas. ¿No ha leído usted a Descartes?<br />
—Me están enfadando con tanto cartesianismo.<br />
—Es natural sentir enfado ante la pérdida de un ser querido —contesta el doctor.<br />
—¿Me está diciendo entonces que mi marido está muerto?<br />
—Mi obligación como médico es respetar los deseos del paciente. Si él dice que está muerto, ¿quién soy yo para defender lo contrario?<br />
—¿Entonces no hay nada que hacer?<br />
—Bueno, hay un par de opciones.<br />
—¿Cuáles? —pregunta esperanzada Marta.<br />
—Enterrarlo o incinerarlo.<br />
—¡Pero eso es una salvajada!<br />
—No, es el procedimiento normal. ¿Qué pretende hacer con un muerto? ¿Meterlo en el congelador? No es legal.<br />
—Miguel, que te van a enterrar, ¿es que no vas a decir nada?<br />
—Yo prefiero que me incineren —contesta él.<br />
—¿Pero qué dices?<br />
—Piénsalo: es mucho más práctico e higiénico. Incinerado me puedes guardar en cualquier sitio. Me puedes poner sobre el televisor, por ejemplo, presidiendo la habitación. No puedes hacer lo mismo con un ataúd, no está permitido y además el olor sería insoportable y espantaría a las visitas.<br />
Aparte de esto, si me incineran puedes destapar la urna de vez en cuando para que me dé el sol, cosa que no puedes hacer si me entierran en un cementerio. Bueno, sí puedes, pero acabas en comisaría si lo haces.<br />
Y la verdad es que prefiero dejar con hambre a los gusanos. Como ves, no hay más que ventajas.<br /></p>
