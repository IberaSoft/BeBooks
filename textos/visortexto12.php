          <p>Boris Stravinski tenía sueño, pero no podía dormir. O quería dormir, pero no tenía sueño. No lo tenía nada claro. Se acostó literalmente. Esto es, en una litera. Luego probó lateralmente, que era más cómodo. Dos horas después decidió que en esa postura no iba a conseguir dormirse. Se levantó, se rascó la nuca, echó a andar. No llegó muy lejos, pues la habitación era pequeña. Se detuvo frente a una estantería llena de libros. Quizá leyendo le entrara sueño, pensó. Cogió un volumen de las memorias de Fray Gregorio de Leopoldo de María, misionero en Cincinnati. Un rato después dejó de leer; se estaba divirtiendo y el objetivo era lo contrario. Se sintió un tanto ridículo con tanto insomnio. Claro, se suponía que dormir era coser y cantar, pero es que ambas cosas se le daban fatal: nunca había conseguido la coordinación necesaria para hacer las dos cosas a la vez sin pincharse o desafinar.<br />
Encendió la tele. En las noticias hablaban de un tiroteo en una juguetería entre dos bandas de niños. Había siete heridos y un secuestrado. Luego informaron de un asesinato. Al parecer, un hombre había matado a su familia por mandato divino. Afirmaba que se lo habían dicho las palomitas mientras se hacían en el microondas, en código morse. A Boris Stravinski le entró hambre, pero no sueño.

