          <p>Cuento del soldado <br />Cuando recuerdo aquel día aún me entran escalofríos. Era una dulce mañana primaveral de 1775 en la que yo me encontraba recostado en el techo escribiendo una alegre sonata para la hija del tendero. Entonces entró por mi ventana un policía y me dijo que estaba perturbando el orden público. Yo no sé lo que dije, pero a ambos nos sonó mal. Así que me detuvo y me encerró durante tres largos años en los que desarrollé un inestable talento musical. Al salir de la cárcel supe que yo era El Hombre Nuevo, ése que anunciaban los dados en las cantinas, ése que se batiría con las olas lunares y vencería. Por eso me presenté en el ejército y me alisté. A mi regimiento lo mandaron a las Bahamas a veranear. Recuerdo que aquella fue una campaña atroz, tuvimos muchas bajas, a pesar de los refuerzos ingentes que nos envió la comandancia tuvimos que retirarnos dejando tras nosotros un paisaje desolador: toallas en la arena, sombrillas, crema solar, bañadores, etc. Muchos de nosotros tuvimos que pasar por la enfermería a causa de nuestro perfecto bronceado. <br />
Una vez de regreso descubrimos que nuestro país había cambiado mucho: los hippies habían llegado al poder e instauraban leyes ecologistas; cada pequeño comerciante debía donar a un miembro de su familia para ser utilizado como abono. Mi anciano padre se despidió de nosotros entre lágrimas después de haber intentado con todas sus fuerzas que saliera elegido yo. <br />
Entonces estalló la guerra. Al parecer la marina suiza había atacado a nuestra flota pesquera y huido hacia Ginebra con todo un cargamento de atún. Tal afrenta no podía quedar sin respuesta. Nuestro gobierno declaró la guerra a Suiza y le conminó a devolver todo atún, toda sardina, todo arenque que nos hubieran arrebatado. El país estaba alborozado, se organizaron manifestaciones de apoyo a la guerra en todo el país, los escolares se alistaban y eran enviados a primera línea de batalla, las mujeres reían como locas y se acostaban con los soldados al grito de hurra. Nuestro regimiento, el más aguerrido de todos, recibió la orden de efectuar ataques de distracción, por lo que fuimos enviados a Mongolia. Llegamos a Ulan Bator una noche estrellada en la que el viento soplaba con fuerza y los mandos nos insultaban a voz en grito. Contactamos con el embajador, que nos comunicó que no había actividad del ejército suizo en el país. Eso nos hizo sospechar que era un espía, por lo que le fusilamos al amanecer. Un par de días después nos pateábamos Mongolia en busca de los suizos, pero no llegamos a encontrar a ninguno. ¿Habrían huido a China? Sí, tenía que ser eso. Invadimos China pasando a cuchillo a los campesinos por colaborar con los suizos. Eso creó un grave conflicto diplomático entre China y nuestro país. De nuevo, la gente estaba alborozada, se organizaron manifestaciones de apoyo al degüelle de chinos en todo el país, los turistas chinos eran apedreados por los escolares, las mujeres reían como locas y se acostaban con los soldados al grito de hurra. <br />
Inesperadamente, nuestro país se rindió y el ejército fue suprimido por falta de fondos. Ahí estaba yo, considerado un asesino en China y, lo que era peor, sin trabajo.

