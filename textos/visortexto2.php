          >—Créetelo, te llamo desde la escuela de espiritismo.<br />—¿Pero cómo va a ser posible hablar con las distintas encarnaciones de uno mismo? Eso es brujería. O peor: narcisismo.<br />—No tengas miedo, vengo en son de paz —dice el alumno, que ha comprendido que una mente primitiva no está preparada para entender que la comunicación entre la vida y la muerte es posible a través de un vaso y un tablero.<br />
